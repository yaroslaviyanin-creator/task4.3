#pragma once

#include <stddef.h>

// Узел односвязного списка для разрешения коллизий в хеш-таблице
typedef struct HashNode {
    char* key;              // Указатель на строку-ключ
    int count;              // Частота встречаемости слова в файле
    struct HashNode* next;  // Указатель на следующий элемент в цепочке
} HashNode;

// Структура ассоциативного массива (хеш-таблицы)
typedef struct HashTable {
    HashNode** buckets;     // Динамический массив указателей на узлы (бакеты)
    size_t size;            // Текущий размер массива бакетов
    size_t count;           // Общее количество элементов, хранящихся в таблице
} HashTable;

// Функция вычисляет хеш-сумму для строки по алгоритму djb2
// key - указатель на нуль-терминированную строку
size_t hash_djb2(const char* key);

// Функция создает новую хеш-таблицу
// size - начальный размер массива бакетов
HashTable* ht_create(size_t size);

// Функция удаляет хеш-таблицу и освобождает память
// table - указатель на удаляемую хеш-таблицу
void ht_destroy(HashTable* table);

// Функция добавляет строку в таблицу или увеличивает счетчик, если она уже есть
// table - указатель на хеш-таблицу
// key - строка-ключ для добавления
// Возвращает 1 в случае успеха, 0 при ошибке выделения памяти
int ht_insert(HashTable* table, const char* key);